    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String filterText = jTextField1.getText().toLowerCase(); // Convierte a minúsculas para hacer una búsqueda insensible a mayúsculas/minúsculas

if (!filterText.isEmpty()) {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    TableRowSorter<DefaultTableModel> rowSorter = new TableRowSorter<>(model);
    jTable1.setRowSorter(rowSorter);

    RowFilter<DefaultTableModel, Object> rowFilter = new RowFilter<DefaultTableModel, Object>() {
        @Override
        public boolean include(Entry<? extends DefaultTableModel, ? extends Object> entry) {
            int columnCount = entry.getModel().getColumnCount();
            for (int column = 0; column < columnCount; column++) {
                Object cellValue = entry.getValue(column);
                if (cellValue != null) {
                    String stringValue = cellValue.toString().toLowerCase(); // Convierte a minúsculas para hacer una búsqueda insensible a mayúsculas/minúsculas
                    if (stringValue.contains(filterText)) {
                        return true; // Filtrar si se encuentra la palabra clave en esta celda
                    }
                }
            }
            return false; // No filtrar si no se encuentra la palabra clave en ninguna celda
        }
    };

    rowSorter.setRowFilter(rowFilter);
} else {
    // Si el campo de filtro está vacío, muestra todos los registros
    jTable1.setRowSorter(null);
}
    }//GEN-LAST:event_jButton3ActionPerformed
    
